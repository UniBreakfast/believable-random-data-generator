const uniqueSorted = (arr) => {
  // removes duplicates from an array of primitives and sorts it
  const set = new Set(arr),
        uniqueArr = Array.from(set)
  return uniqueArr.sort()
}

const concatBy = (arr, lineLen) => {
  // makes a multiline string by line length to save an array in this file
  arr = arr.map(word=>word+',')
  let word, lines='', line=''
  while (word = arr.shift()) {
    if ( (line+word).length > lineLen ) {
      lines += '\n  '+line
      line = word
    }
    else line += word
  }
  return lines.trim()+'\n  '+line.replace(/,$/,'')
}

